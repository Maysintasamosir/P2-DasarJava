public class C_incDec {
    public static void main(String[] args) {
        //melakukan increment dan decremen nilai
        int x=8,y=13;
        System.out.println("x="+x);
        System.out.println("y="+y);
        System.out.println("y="+y++);
        System.out.println("x="+x--);
        System.out.println("y="+--y);







    }
}
