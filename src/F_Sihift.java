public class F_Sihift {
    public static void main(String[] args) {
        //melakukan operasi shift
        int x=7;
        System.out.println("x="+x);
        System.out.println("x>>2="+(x>>2));
        System.out.println("x<<1="+(x<<1));
        System.out.println("x>>>1="+(x>>>2));



    }
}
